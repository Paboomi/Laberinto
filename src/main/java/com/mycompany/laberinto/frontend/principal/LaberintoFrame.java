    private void showMazeSelected(String name) {
        pnlLaberinto.removeAll();
        GenerateMazeFromList mazeFromList = new GenerateMazeFromList(lista, name, jugador);
        mazeFromList.setSize(pnlLaberinto.getWidth(), pnlLaberinto.getHeight());
        pnlLaberinto.add(mazeFromList, BorderLayout.CENTER);
        mostrarPanel("Principal");
        GenerateMazeFromList lab = (GenerateMazeFromList) pnlLaberinto.getComponent(0);
        lab.requestFocusInWindow();
        pnlLaberinto.revalidate();
        pnlLaberinto.repaint();
    }

    private void construirTablero() {
        int x, y;

        x = Integer.parseInt(jtxtLargoLaberinto.getText());
        y = Integer.parseInt(jtxtAnchoLaberinto.getText());
        tablero = new TableroParaCrearLaberinto(y, x);
        tablero.setVisible(true);
        limpiarCreadorLaberinto();

    }

    private void repintarTablero() {
        int x, y;

        x = Integer.parseInt(jtxtColumnaMakeMaze.getText());
        y = Integer.parseInt(jtxtFilaMakeMaze.getText());
        if (rbtnCamino.isSelected()) {
            tablero.aplicarCasillaCamino(y, x, 1);
        } else if (rbtnPared.isSelected()) {
            tablero.aplicarCasillaPared(y, x, 2);
        }else if (rbtnInicio.isSelected()){
            tablero.aplicarCasillaInicio(y, x, 3);
        }else if (rbtnSalida.isSelected()){
            int cantOroSalida = Integer.parseInt(jtxtOroSalir.getText());
            tablero.aplicarCasillaSalida(y, x, 4, cantOroSalida);
        }else if (rbtnOro.isSelected()){
            int cantOroCasilla = Integer.parseInt(jtxtCantOroCasilla.getText());
            tablero.aplicarCasillaOro(y, x, 5, cantOroCasilla);
        }
        limpiarCreadorLaberinto();
    }

    private void limpiarCreadorLaberinto() {
        pnlCreatingMaze.removeAll();
        pnlCreatingMaze.add(tablero, BorderLayout.CENTER);
        pnlCreatingMaze.revalidate();
        pnlCreatingMaze.repaint();
    }
    
