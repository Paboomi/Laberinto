    //Metodo para llenar el combobox 
    private void llenarCombobox(JComboBox comboBox) {
        lista.cargarLaberintosDesdeArchivos();
        NodoLaberinto actual = lista.getInicio();
        //Ciclo que termina hasta que la ultima referencia apunte a null
        while (actual != null) {
            //Obtenemos el nombre
            String nameMaze = actual.getLaberinto().getNombre();
            //Agregamos el nombre al combobox
            comboBox.removeAllItems();
            comboBox.addItem(nameMaze);
            //Obtenemos la siguiente referencia
            actual = actual.getSiguiente();
        }
    }

